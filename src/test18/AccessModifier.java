package test18;

public class AccessModifier {
	protected int a = 10;
	/*
	 * 기본적으로 접근제어자는 총 4개가 있음
	 * public protected default private
	 * 단 default는 접근제어라를 아무것도 안쓴게 default임
	 * public : 그냥 다됨~~ 아 몰랑~ 다됨~
	 * protected : 같은패키지내 또는 상속받은 클래스에서 사용할 수 있음
	 * default : 같은 패키지에서 됨
	 * private : 해당 클래스에서만 사용할 수 있음!! 다른데선 죽어도 안됨!!
	 */
}
